  gotoLogin() {
    _controller.animateToPage(
      0,
      duration: Duration(milliseconds: 750),
      curve: Curves.easeInOut,
    );
  }

  gotoSignup() {
    _controller.animateToPage(
      2,
      duration: Duration(milliseconds: 750),
      curve: Curves.easeInOut,
    );
  }

